/**
 * 戦型名 → 上位概念（親カテゴリ）の対応表。
 *
 * 分析画面の戦型別成績で、下位概念の戦型を上位概念にまとめて集計するために使う。
 * - 複数の要素を含む戦型名は「囲い」を親とする（例：雁木袖飛車早繰り銀 → 雁木）
 * - この表に載っていない戦型名は、そのまま独立した戦型として集計される
 *
 * 新しい戦型をまとめたい場合は、この表に「子の戦型名: "親の戦型名"」の行を追加する。
 */
export const STRATEGY_PARENT_MAP: Record<string, string> = {
  // 矢倉系
  急戦矢倉: "矢倉",
  矢倉急戦: "急戦矢倉",
  急戦矢倉早繰り銀: "急戦矢倉",
  持久戦矢倉: "矢倉",
  矢倉囲い: "矢倉",
  角換わりからの矢倉: "矢倉",
  角換わりからの矢倉囲い: "矢倉",
  // 右玉系
  雁木右玉: "右玉",
  角換わり右玉: "右玉",
  対振り右玉: "右玉",
  // 囲いを親とするもの
  雁木袖飛車早繰り銀: "雁木",
  雁木右四間飛車: "雁木",
  左美濃右四間飛車: "左美濃",
  カニ囲い右四間飛車: "カニ囲い",
  // elmo系（正式表記はアルファベットの「elmo」）
  右四間飛車elmo: "elmo",
  右四間飛車エルモ: "elmo",
  elmo囲い: "elmo",
  エルモ囲い: "elmo",
  エルモ: "elmo",
  // 石田流系
  石田流本組: "石田流",
  早石田: "石田流",
  升田式石田流: "石田流",
  // 駒落ち定跡系
  飛車落ち上手定跡: "上手定跡",
};

/** 戦型名から親カテゴリ名を返す。対応表にない場合はそのまま返す。 */
export function resolveParentStrategy(name: string): string {
  return STRATEGY_PARENT_MAP[name] ?? name;
}
