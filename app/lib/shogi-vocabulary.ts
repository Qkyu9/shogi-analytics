/**
 * 文字起こしAPI（OpenAI）の prompt パラメータ用ヒント。
 * 「これから来る音声にはこういう単語が出る」と事前に伝えて誤認識を減らす。
 * whisper-1 はプロンプトが長いと先頭から切り捨てられるため、重要な戦型名を後方に置く。
 */
export function buildTranscribeHint(): string {
  return `将棋の対局の振り返り。${[...GENERAL_TERMS, ...STRATEGY_TERMS].join(
    "、"
  )}。`;
}

/** AI補正プロンプト用：正式用語の一覧テキスト */
export function formatVocabularyForPrompt(): string {
  return [
    `- 戦型・囲い: ${STRATEGY_TERMS.join("、")}`,
    `- その他: ${GENERAL_TERMS.join("、")}`,
  ].join("\n");
}
