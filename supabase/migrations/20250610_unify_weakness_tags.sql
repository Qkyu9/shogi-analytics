-- 敗因タグの同義語を正本表記に統一（手動で付けた固有タグは対象外）

update public.game_records
set
  tags = (
    select coalesce(
      array_agg(distinct case
        when t in (
          '序盤の手筋',
          '序盤の手筋選択',
          '序盤の手筋ミス',
          '駒組みの工夫',
          '駒組みの工夫不足'
        ) then '序盤の駒組み'
        when t in (
          '詰め逃し・逆転の見落とし',
          '積み逃し・逆転の見落とし',
          '詰めの力不足',
          '詰みの力不足',
          '詰め力不足'
        ) then '詰み逃し・逆転の見落とし'
        when t in (
          '中盤の攻受判断ミス',
          '中盤の攻受判断',
          '中盤の攻防判断ミス',
          '中盤の攻防判断'
        ) then '中盤の読み（攻めるか受けるか）'
        when t = '中盤の勝ち切り不足' then '勝ち切りの手筋不足'
        else t
      end),
      '{}'::text[]
    )
    from unnest(tags) as t
  ),
  updated_at = now()
where tags && array[
  '序盤の手筋',
  '序盤の手筋選択',
  '序盤の手筋ミス',
  '駒組みの工夫',
  '駒組みの工夫不足',
  '詰め逃し・逆転の見落とし',
  '積み逃し・逆転の見落とし',
  '詰めの力不足',
  '詰みの力不足',
  '詰め力不足',
  '中盤の攻受判断ミス',
  '中盤の攻受判断',
  '中盤の攻防判断ミス',
  '中盤の攻防判断',
  '中盤の勝ち切り不足'
]::text[];
